import UIKit

extension Int{
    
    var CF:CGFloat{
      return CGFloat(self)
    }
    
    var F:Float{
        return Float(self)
    }
    
    var D:Double{
        return Double(self)
    }
}

extension CGFloat{
    
    var I:Int{
        return Int(self)
    }
}
